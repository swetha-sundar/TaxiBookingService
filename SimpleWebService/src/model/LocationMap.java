public class LocationMap {
    private int x; //x-coordinate
    private int y; //y-coordinate

    public LocationMap(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public Integer getX() {
        return this.x;
    }

    public Integer getY() {
        return this.y;
    }

}
